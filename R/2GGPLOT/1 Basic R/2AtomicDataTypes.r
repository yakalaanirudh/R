#Atomic Data Types
#There are 5 types

#Character
#Numeric(double)
#Integer
#Complex
#Logical

#Character
a="c"
aa="hello"
typeof(a)           #character
typeof(aa)          #character


#Numeric
b=18
bb=10.7
typeof(a)           #double
typeof(aa)          #double

class(b)            #numeric
class(bb)           #numeric

#By default any numeric in r is double so to make it an integer we have to specify it
#Integer
c=1L
typeof(c)           #integer


#Complex
d=0+3i
typeof(d)           #complex


#Logical
e=TRUE              
EE=FALSE            
typeof(e)           #Logical
typeof(ee)          #Logical