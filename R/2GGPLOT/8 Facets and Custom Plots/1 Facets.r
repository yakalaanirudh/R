#Facets are used to create small multiples without cluttering the main data with too much data(small multiple plots)

#There are three different types of facets
#   1   facet_null()    
#   No facet,single plot, DEFAULT

#   2   facet_grid()    
#   Creates a matrix out of all the possible combinations of the variables specified

#   3   facet_wrap()    
#   Creates a list where we only have panels for the combinations of variables that exist,then it is arranged in 2D format